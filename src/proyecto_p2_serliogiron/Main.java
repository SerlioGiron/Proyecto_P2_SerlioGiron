    public String texto(JTable tabla){
        String temp = "";
        
        temp += "class " + tabla.getValueAt(0, 0) + "{ \n";
        
        for (int i = 1; i < tabla.getRowCount(); i++) {
            
            temp += "\t" + tabla.getValueAt(i, 0) + "\n";
            
        }
        
        return temp;
    }
    
    
    
